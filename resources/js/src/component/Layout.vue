} from "vuex";
import {
    useRouter
} from "vue-router";
import {
    onMounted
} from "vue";

import bgiLogo from '/public/assets/img/bgi_logo.png';

export default {
    name: "Login",
    setup() {
        const store = useStore();
        const router = useRouter();

        const logout = () => {
            store.dispatch("logout")
                .then(() => {
                    router.push({
                        name: "Login"
                    })
                })
        };

        const isAccessTokenAvailable = () => {
            const reqToken = {
                'access_token': localStorage.getItem('access_token')
            };
            store.dispatch('checkToken', reqToken)
                .then(() => {

                });
        };

        const getUser = () => {
            store.dispatch('getUser');
        };

        const userNow = () => {
            return store.state.user.data;
        }

        onMounted(() => {
            isAccessTokenAvailable();
            getUser();
        });

        return {
            logout,
            userNow,
            iconClass: 'bi-heart-fill',
            gambarUrl: bgiLogo
        }
    }
}
</script>

